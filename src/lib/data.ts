function parseDate(dateStr: string): string {
    if (!dateStr || dateStr.trim() === '') return '';
    try {
        // Create a mapping for month abbreviations
        const monthMap: { [key: string]: number } = {
            'Jan': 0, 'Feb': 1, 'Mar': 2, 'Apr': 3, 'May': 4, 'Jun': 5,
            'Jul': 6, 'Aug': 7, 'Sep': 8, 'Oct': 9, 'Nov': 10, 'Dec': 11
        };

        // Split the date string 'dd-Mon-yy'
        const parts = dateStr.split('-');
        if (parts.length !== 3) return '';

        const day = parseInt(parts[0], 10);
        const month = monthMap[parts[1]];
        const year = parseInt(parts[2], 10) + 2000; // Assuming 'yy' is in the 21st century

        if (isNaN(day) || month === undefined || isNaN(year)) return '';

        // Create date object and format to 'YYYY-MM-DD'
        const date = new Date(year, month, day);
        return date.toISOString().split('T')[0];
    } catch (e) {
        return '';
    }
}


